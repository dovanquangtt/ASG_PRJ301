package model;

/**
 *
 * @author admin
 */
public class Employee {

    private int id;
    private String ten;
    private String dob;
    private String email;
    private String phone;
    private int divisionId;
    private int roleId;

    public Employee() {
    }

    public Employee(int id, String ten, String dob, String email, String phone, int divisionId, int roleId) {
        this.id = id;
        this.ten = ten;
        this.dob = dob;
        this.email = email;
        this.phone = phone;
        this.divisionId = divisionId;
        this.roleId = roleId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getDivisionId() {
        return divisionId;
    }

    public void setDivisionId(int divisionId) {
        this.divisionId = divisionId;
    }

    public int getRoleId() {
        return roleId;
    }

    public void setRoleId(int roleId) {
        this.roleId = roleId;
    }

}
